
/**
 * Position class to store the raw x and y coordinates
 * */
public class Position {
    private int x1;
    private int y1;
   
    public Position(int x1, int y1) {
        this.x1 = x1;
        this.y1 = y1;
    }
    
    public int getX() {
    	return x1;
    }
    
    public int getY() {
    	return y1;
    }
}
